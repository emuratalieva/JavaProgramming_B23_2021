package day03_Variables;

/*
    \n: new line (Enter)
    \t: paragraph space (tab)
    \": double quote
    \\: back slash
 */

public class EscapeSequences {

    public static void main(String[] args) {


        System.out.println("Hello everyone \nHow are you all today?");

        System.out.println("---------------------------------------------");

        System.out.println("Hello Everyone");
        System.out.println("My name is Muhtar");
        System.out.println("I live in Virginia");

        System.out.println("-----------------------------------");

        System.out.println("Hello Everyone\nMy name is Muhtar\nI live in Virginia");

        System.out.println("------------------------------------");
        System.out.println("\tToday is a great day to study Java");

        System.out.println("-----------------------------------------------");

        System.out.println("My favorite TV series is \"Game of Throne\"");


        System.out.println("--------------------------------");

        System.out.println("/");   // front slash
        System.out.println("\\");  // back slash

        System.out.println("'Java'");

    }

}
