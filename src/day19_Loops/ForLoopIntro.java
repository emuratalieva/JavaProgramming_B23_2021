package day19_Loops;

public class ForLoopIntro {

    public static void main(String[] args) {

        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        System.out.println("---------------------------------------");

        for(int i = 1;  i <= 10; i++ ){ //i : 1, 3, 5, 7
            System.out.println("Hello World"+ i); //3 times
        }




        System.out.println("Hello Cybertek");



    }

}
